#include "libft.h"
#include <stdlib.h>

void		ft_free2d(void **arr)
{
	int i;

	i = 0;
	while (arr + i && *(arr + i))
	{
		free(*(arr + i));
		*(arr + i++) = NULL;
	}
	if (arr)
		free(arr);
}
