#include "libft.h"

int	ft_islowercase(char *str)
{
	int i;

	i = -1;
	while (str[++i])
		if (!(str[i] >= 'a' && str[i] <= 'z') && ft_isalpha(str[i]))
			return (0);
	return (1);
}
