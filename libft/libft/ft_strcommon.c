#include "libft.h"

int		ft_strcommon(char *s1, char *s2)
{
	int res;

	res = 0;
	if (!s1 || !s2)
		return (0);
	while (*s1 == *s2 && *s1 && *s2)
	{
		res++;
		s1++;
		s2++;
	}
	return (res);
}
